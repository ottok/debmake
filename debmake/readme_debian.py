def readme_debian(para):
    sep = '-' * (len(para['package']) + 11)
    msg = '''\
{0} for Debian
{1}

Please edit this to provide information specific information on 
this {0} Debian package.

    (Automatically generated by {2} Version {3})

 -- {4} <{5}>  {6}
'''.format(
            para['package'], 
            sep, 
            para['program_name'], 
            para['program_version'], 
            para['fullname'], 
            para['email'], 
            para['date'])
    return msg

